async def run():
    # 1. Fix vit_11 meals
    topic = await db.topics.find_one({"key": "vitamins"})
    if topic:
        cards = topic.get("cards", [])
        for card in cards:
            if card["card_id"] == "vit_11":
                card["content"]["meals"] = VIT_11_MEALS
                print("Fixed vit_11 meal plan with nutrient tags")
                break
        await db.topics.update_one({"key": "vitamins"}, {"$set": {"cards": cards}})

    # 2. Apply deep structured translations
    all_topics = await db.topics.find().to_list(100)
    updated = 0
    for topic in all_topics:
        cards = topic.get("cards", [])
        changed = False
        for card in cards:
            cid = card.get("card_id", "")
            if cid in DEEP_TRANS:
                dt = DEEP_TRANS[cid]
                if "translations" not in card:
                    card["translations"] = {}
                for lang in ["ta", "hi"]:
                    if lang not in card["translations"]:
                        card["translations"][lang] = {}
                    if lang in dt:
                        card["translations"][lang].update(dt[lang])
                changed = True
                updated += 1
        if changed:
            await db.topics.update_one({"_id": topic["_id"]}, {"$set": {"cards": cards}})

    print(f"Applied deep structured translations to {updated} cards across all topics")
    print("Done!")

asyncio.run(run())
